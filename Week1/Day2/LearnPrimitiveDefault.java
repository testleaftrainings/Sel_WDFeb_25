package weekOneDayTwo;

public class LearnPrimitiveDefault {

	public static void main(String[] args) {
 
		//default value  the type integer or float 
		
		byte a = 0;
		short b = 0;
		int c = 0;
		double d = 0;
		boolean e = false;
		char f = 0;
		
		System.out.println("default value  the type byte"+a);
		System.out.println("default value  the type short"+b);
		System.out.println("default value  the type int"+c);
		System.out.println("default value  the type double"+d);
		System.out.println("default value  the type boolean"+e);
		System.out.println("default value  the type char"+f);
		
		
	}

}
