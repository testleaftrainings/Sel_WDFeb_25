package WeekOneDayThree;

public class LearnSwitch {

	 static int a=7;
	
	public static void main(String[] args) {
		
		
		switch (a) {
		case 1:
			System.out.println("monday");
			break;
		case 2:
			System.out.println("tueday");
			break;

		default:
			System.out.println("Not included");
			break;
		}

	}

}
