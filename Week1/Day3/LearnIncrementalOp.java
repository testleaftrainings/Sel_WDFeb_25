
package WeekOneDayThree;

public class LearnIncrementalOp {

	public static void main(String[] args) {
		
		int i=0;
		System.out.println("post incremental operator works");
		System.out.println("uses the value ");
		System.out.println(i++);
		System.out.println("updates the value ");
		System.out.println(i);
		
		int j=0;
		System.out.println("pre incremental operator works");
		System.out.println("updates the value ");
		System.out.println("uses the value ");
		System.out.println(++j);
		System.out.println("updated the value ");
		System.out.println(j);

		

	}

}
